public class Testmultiples {
    public static void main(String[] args){
        for(int i=0;i<=5;i++){
            System.out.println(i*2+" ");
        }
        System.out.println("");
        for(int i=0;i<=5;i++){
            System.out.println(i*2+1+" ");
        }
        System.out.println("");
        for(int j=3;j<101;j++){
            for(int i=0;i<=10;i++){
                System.out.println(i*j+" ");
            }
            System.out.println();
            }
        }
    }
    
